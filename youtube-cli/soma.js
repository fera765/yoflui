/**
 * Função que soma dois números
 * @param {number} a - Primeiro número
 * @param {number} b - Segundo número
 * @returns {number} - A soma dos dois números
 */
function soma(a, b) {
    return a + b;
}

// Exemplo de uso:
// soma(5, 3) retorna 8
// soma(2.5, 1.5) retorna 4
// soma(-1, 5) retorna 4